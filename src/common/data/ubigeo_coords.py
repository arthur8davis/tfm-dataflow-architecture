def normalize_text(text):
    """Normaliza texto quitando acentos para comparación"""
    import unicodedata
    if not text:
        return text
    # Normalizar a NFD (separar caracteres base de acentos) y filtrar acentos
    normalized = unicodedata.normalize('NFD', text)
    return ''.join(c for c in normalized if unicodedata.category(c) != 'Mn')


# Crear versiones normalizadas de los diccionarios para búsqueda
PROVINCIA_COORDS_NORMALIZED = {normalize_text(k): v for k, v in PROVINCIA_COORDS.items()}
DISTRITO_COORDS_NORMALIZED = {normalize_text(k): v for k, v in DISTRITO_COORDS.items()}


def get_coords_from_ubigeo(departamento=None, provincia=None, distrito=None):
    """
    Obtiene coordenadas usando distrito, provincia o departamento (en orden de prioridad).

    Args:
        departamento: Nombre del departamento
        provincia: Nombre de la provincia
        distrito: Nombre del distrito (más preciso)

    Returns:
        dict con lat y lon, o None si no se encuentra
    """
    # Prioridad 1: Buscar por distrito (clave compuesta DEPARTAMENTO|PROVINCIA|DISTRITO)
    if distrito and provincia and departamento:
        key = f"{departamento.upper().strip()}|{provincia.upper().strip()}|{distrito.upper().strip()}"
        key_normalized = normalize_text(key)
        if key_normalized in DISTRITO_COORDS_NORMALIZED:
            return DISTRITO_COORDS_NORMALIZED[key_normalized]

    # Prioridad 2: Buscar por provincia
    if provincia:
        prov_normalized = normalize_text(provincia.upper().strip())
        if prov_normalized in PROVINCIA_COORDS_NORMALIZED:
            return PROVINCIA_COORDS_NORMALIZED[prov_normalized]

    # Prioridad 3: Buscar por departamento
    if departamento:
        dept_normalized = normalize_text(departamento.upper().strip())
        if dept_normalized in PROVINCIA_COORDS_NORMALIZED:
            return PROVINCIA_COORDS_NORMALIZED[dept_normalized]

    return None
